# Testando Operadores

n = int(input('Digite um Número: '))

print('O dobro de {} vale {}.'.format(n, (n*2)))
print('O Triplo de {} vale {}.'.format(n, (n*3)))
print('A Raiz Quadrada de {} é igual a {}.'.format(n, pow(n, (1/2))))
print('A Pôtencia de {} é igual a {}.'.format(n, (n**2)))


