algo = input('Digite um Algo: ')

print('O tipo primitivo desse valor é: ', type(algo))
print('O valor citado só tem espaços: ', algo.isspace())
print('E um Número: ', algo.isnumeric()) 
print('É Alfabético: ', algo.isalpha())
print('É Alfanúmerico: ', algo.isalnum())
print('Esta em Maiúsculas: ', algo.isupper())
print('Está em Minúsculas: ', algo.islower())
print('Esta palavra está capitalizada: ', algo.istitle())

