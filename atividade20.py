# Faça um programa que inicialize uma lista de compras com 5 itens diferentes e exiba todos utilizando um laço de repetição while.

# Inicializando a lista de compras com 5 itens diferentes
lista_de_compras = ["Arroz", "Feijão", "Açucar", "Café", "Leite"]

# Inicializando o contador
i = 0

# Exibindo todos os itens da lista usando um laço de repetição while
print("Lista de Compras:")
while i < len(lista_de_compras):
    print(lista_de_compras[i])
    i += 1  # Incrementa o contador
