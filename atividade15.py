# Faça um programa que inicialize uma lista de compras com 5 itens diferentes e exiba todos utilizando um laço de repetição

# Inicializando a lista de compras com 5 itens diferentes
lista_de_compras = ["Arroz", "Feijão", "Açucar", "Café", "Leite"]

# Exibindo todos os itens da lista usando um laço de repetição
print("Lista de Compras:")
for item in lista_de_compras:
    print(item)
